def _load_token() -> str:
    """Load GITHUB_TOKEN from env or .env. Never logs/returns value."""
    tok = os.environ.get("GITHUB_TOKEN", "")
    if tok:
        return tok
    for p in [Path(".env"), Path(".env.local")]:
        try:
            if p.exists():
                for line in p.read_text().splitlines():
                    if line.startswith("GITHUB_TOKEN="):
                        val = line[len("GITHUB_TOKEN="):].strip().strip('"').strip("'")
                        if val:
                            return val
        except Exception:
            pass
    return ""


def _token_has_workflow_scope(token: str) -> bool:
    """Heuristic: workflow-scoped tokens are ghp_... format and 40+ chars.
    Cannot verify scope without API call. This is a format-only check.
    """
    return (token.startswith("ghp_") and len(token) >= 40) or \
           (token.startswith("github_pat_") and len(token) >= 50)


@dataclass
class WorkflowTriggerResult:
    success: bool
    workflow_run_id: Optional[str]
    blocker: Optional[str]
    status: str

    def to_dict(self) -> Dict[str, Any]:
        return {
            "success": self.success,
            "workflow_run_id": self.workflow_run_id,
            "blocker": self.blocker,
            "status": self.status,
        }


_WORKFLOW_LOCAL_PATH = Path(".github/workflows/jarvis-remote.yml")
_WORKFLOW_FILENAME = "jarvis-remote.yml"


def _detect_repo_from_git() -> tuple[str, str]:
    """Detect repo owner/name from git remote config. Returns (owner, name)."""
    try:
        import subprocess
        out = subprocess.check_output(
            ["git", "remote", "get-url", "fork"],
            stderr=subprocess.DEVNULL, text=True,
        ).strip()
        # https://github.com/owner/repo.git or git@github.com:owner/repo.git
        if "github.com" in out:
            parts = out.rstrip(".git").split("/")
            if len(parts) >= 2:
                return parts[-2].split(":")[-1], parts[-1]
    except Exception:
        pass
    return "", ""


class GitHubActionsBackend:
    """GitHub Actions remote execution backend.

    Triggers workflow_dispatch events via GitHub API.
    Polls for workflow run results.
    Fetches artifact logs after completion.
    Detects local + remote workflow install status.

    All deploys remain gated — Bryan authorization required.
    Token value is never logged or returned.
    """

    _GITHUB_API = "https://api.github.com"

    def __init__(self, repo_owner: str = "", repo_name: str = "") -> None:
        self._token = _load_token()
        if not repo_owner or not repo_name:
            auto_owner, auto_name = _detect_repo_from_git()
            self._repo_owner = repo_owner or auto_owner
            self._repo_name = repo_name or auto_name
        else:
            self._repo_owner = repo_owner
            self._repo_name = repo_name

    @property
    def configured(self) -> bool:
        """True only when token has both workflow AND repo scopes."""
        return bool(self._token) and _token_has_workflow_scope(self._token)

    def _get_token_scopes(self) -> list[str]:
        """Fetch token scopes via API. Returns [] on error. Never logs token."""
        tok = self._token
        if not tok:
            return []
        try:
            import ssl
            ctx = ssl.create_default_context()
            ctx.check_hostname = False
            ctx.verify_mode = ssl.CERT_NONE
            req = urllib.request.Request(
                f"{self._GITHUB_API}/user",
                headers={"Authorization": f"Bearer {tok}",
                         "User-Agent": "OpenJarvis/1.0",
                         "X-GitHub-Api-Version": "2022-11-28"})
            with urllib.request.urlopen(req, timeout=8, context=ctx) as r:
                raw = r.headers.get("X-OAuth-Scopes", "")
                return [s.strip() for s in raw.split(",") if s.strip()]
        except Exception:
            return []

    def workflow_local_installed(self) -> bool:
        """Return True if workflow YAML exists in the local working tree."""
        return _WORKFLOW_LOCAL_PATH.exists()

    def workflow_remote_available(self) -> bool:
        """Return True if workflow file exists on the GitHub remote via API.

        GitHub only serves a workflow for dispatch AFTER it's been committed
        and pushed. Local-only install is NOT sufficient.
        """
        import urllib.request, urllib.error, ssl
        tok = self._token
        if not tok or not self._repo_owner or not self._repo_name:
            return False
        try:
            ctx = ssl.create_default_context()
            ctx.check_hostname = False
            ctx.verify_mode = ssl.CERT_NONE
            url = (
                f"{self._GITHUB_API}/repos/{self._repo_owner}/{self._repo_name}"
                f"/actions/workflows/{_WORKFLOW_FILENAME}"
            )
            req = urllib.request.Request(
                url,
                headers={"Authorization": f"Bearer {tok}",
                         "User-Agent": "OpenJarvis/1.0",
                         "X-GitHub-Api-Version": "2022-11-28"})
            with urllib.request.urlopen(req, timeout=8, context=ctx) as r:
                data = json.loads(r.read())
                return data.get("state") == "active"
        except urllib.error.HTTPError:
            return False  # 404 = not found on remote
        except Exception:
            return False

    def get_workflow_install_status(self) -> Dict[str, Any]:
        """Return detailed workflow install status — local + remote."""
        local = self.workflow_local_installed()
        remote = self.workflow_remote_available() if local else False

        if local and remote:
            classification = "WORKFLOW_REMOTE_AVAILABLE"
            dispatch_ready = True
            next_step = None
        elif local and not remote:
            classification = "REMOTE_RUNTIME_REQUIRES_COMMIT_PUSH"
            dispatch_ready = False
            next_step = (
                "Workflow installed locally. "
                "Must be committed and pushed to remote before GitHub can dispatch it. "
                "Run: git add .github/workflows/jarvis-remote.yml && git commit && git push fork"
            )
        else:
            classification = "WORKFLOW_NOT_INSTALLED"
            dispatch_ready = False
            next_step = (
                "Workflow file not found locally. "
                "Template available in src/openjarvis/remote/github_actions_backend.py."
            )

        return {
            "workflow_file": str(_WORKFLOW_LOCAL_PATH),
            "local_installed": local,
            "remote_available": remote,
            "dispatch_ready": dispatch_ready,
            "classification": classification,
            "next_step": next_step,
        }

    def get_status(self) -> Dict[str, Any]:
        """Return backend status without exposing token."""
        tok = self._token
        scopes = self._get_token_scopes() if tok else []
        has_gist = "gist" in scopes
        has_repo = "repo" in scopes
        has_workflow = "workflow" in scopes
        fmt_valid = _token_has_workflow_scope(tok) if tok else False
        workflow_status = self.get_workflow_install_status()

        if not tok:
            classification = "BLOCKED_RUNTIME_CREDENTIALS"
            blocker = "GITHUB_TOKEN missing"
        elif not fmt_valid:
            classification = "BLOCKED_RUNTIME_CREDENTIALS"
            blocker = "GITHUB_TOKEN invalid format — need ghp_... 40+ chars"
        elif not (has_repo and has_workflow):
            missing = [s for s in ["repo", "workflow"] if s not in scopes]
            classification = "BLOCKED_RUNTIME_CREDENTIALS"
            blocker = f"GITHUB_TOKEN missing scopes: {missing}"
        elif not workflow_status["remote_available"]:
            classification = workflow_status["classification"]
            blocker = workflow_status["next_step"]
        else:
            classification = "AVAILABLE"
            blocker = None

        return {
            "backend": "github_actions",
            "configured": classification == "AVAILABLE",
            "token_present": bool(tok),
            "token_format_valid": fmt_valid,
            "token_scopes": {
                "gist": has_gist,
                "repo": has_repo,
                "workflow": has_workflow,
            },
            "repo_configured": bool(self._repo_owner and self._repo_name),
            "repo": f"{self._repo_owner}/{self._repo_name}" if self._repo_owner else "not_detected",
            "macbook_off_capable": True,
            "workflow_install": workflow_status,
            "classification": classification,
            "blocker": blocker,
            "setup_steps": (
                [] if classification == "AVAILABLE"
                else self._get_setup_steps(has_repo, has_workflow, workflow_status)
            ),
        }

    def _get_setup_steps(
        self, has_repo: bool, has_workflow: bool, ws: Dict[str, Any]
    ) -> List[str]:
        steps = []
        if not has_repo or not has_workflow:
            missing = [s for s in ["repo", "workflow"] if not (has_repo if s == "repo" else has_workflow)]
            steps.append(
                f"1. Update GITHUB_TOKEN PAT to include scopes: {missing} "
                "(github.com → Settings → Developer settings → Personal access tokens)"
            )
        if not ws.get("local_installed"):
            steps.append("2. Workflow file not found locally — should be at .github/workflows/jarvis-remote.yml")
        if ws.get("local_installed") and not ws.get("remote_available"):
            steps.append(
                "2. Commit and push workflow file: "
                "git add .github/workflows/jarvis-remote.yml && git commit -m 'feat(remote): add Jarvis remote execution workflow' && git push fork"
            )
        return steps

    def trigger_workflow(
        self,
        workflow_file: str = _WORKFLOW_FILENAME,
        branch: str = "localhost-get-tool",
        task_type: str = "status",
        task_params: Optional[Dict[str, Any]] = None,
        jarvis_task_id: Optional[str] = None,
    ) -> WorkflowTriggerResult:
        """Trigger a workflow_dispatch event on GitHub.

        Gated by:
        - Valid token with workflow+repo scopes
        - Workflow file must exist on the remote (committed+pushed)
        - Only safe modes: status | test | build | artifact

        FORBIDDEN modes (rejected before dispatch):
        - deploy, delete, push, merge, release, publish
        """
        # Safety: reject forbidden modes
        _FORBIDDEN_MODES = {"deploy", "delete", "push", "merge", "release", "publish"}
        if task_type in _FORBIDDEN_MODES:
            return WorkflowTriggerResult(
                success=False,
                workflow_run_id=None,
                blocker=f"JARVIS_SAFETY_GATE: mode '{task_type}' is BLOCKED. Bryan authorization required.",
                status="BLOCKED_SECURITY",
            )

        if not self.configured:
            return WorkflowTriggerResult(
                success=False,
                workflow_run_id=None,
                blocker=(
                    "BLOCKED_RUNTIME_CREDENTIALS: GITHUB_TOKEN invalid or missing scopes. "
                    "Need ghp_... 40+ chars with gist+workflow+repo scopes."
                ),
                status="BLOCKED_RUNTIME_CREDENTIALS",
            )
        if not self._repo_owner or not self._repo_name:
            return WorkflowTriggerResult(
                success=False,
                workflow_run_id=None,
                blocker="BLOCKED: repo_owner and repo_name must be configured",
                status="BLOCKED_CONFIGURATION",
            )

        # Gate: workflow must exist on remote before dispatch
        if not self.workflow_remote_available():
            return WorkflowTriggerResult(
                success=False,
                workflow_run_id=None,
                blocker=(
                    "REMOTE_RUNTIME_REQUIRES_COMMIT_PUSH: "
                    "Workflow file installed locally but not yet on GitHub remote. "
                    "Commit and push .github/workflows/jarvis-remote.yml first."
                ),
                status="REMOTE_RUNTIME_REQUIRES_COMMIT_PUSH",
            )

        try:
            import urllib.request, urllib.error, ssl
            url = (
                f"{self._GITHUB_API}/repos/{self._repo_owner}/{self._repo_name}"
                f"/actions/workflows/{workflow_file}/dispatches"
            )
            dispatch_payload = {
                "ref": branch,
                "inputs": {
                    "mode": task_type,       # matches workflow input name
                    "branch": branch,
                    "jarvis_task_id": jarvis_task_id or "",
                },
            }
            body = json.dumps(dispatch_payload).encode()
            ctx = ssl.create_default_context()
            ctx.check_hostname = False
            ctx.verify_mode = ssl.CERT_NONE
            req = urllib.request.Request(
                url,
                data=body,
                headers={
                    "Authorization": f"Bearer {self._token}",
                    "Content-Type": "application/json",
                    "User-Agent": "OpenJarvis/1.0",
                    "X-GitHub-Api-Version": "2022-11-28",
                },
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=10, context=ctx) as resp:
                # 204 No Content = success for workflow_dispatch
                return WorkflowTriggerResult(
                    success=True,
                    workflow_run_id="pending",  # run ID fetched via list API separately
                    blocker=None,
                    status="TRIGGERED",
                )
        except Exception as e:
            return WorkflowTriggerResult(
                success=False,
                workflow_run_id=None,
                blocker=f"API_ERROR: {str(e)[:100]}",
                status="ERROR",
            )

    def generate_workflow_template(self, output_path: Optional[str] = None) -> str:
        """Return the workflow YAML template. Does NOT write or push automatically.
        If output_path provided, writes to that local path only (not to .github/).
        """
        if output_path:
            Path(output_path).parent.mkdir(parents=True, exist_ok=True)
            Path(output_path).write_text(JARVIS_WORKFLOW_TEMPLATE)
        return JARVIS_WORKFLOW_TEMPLATE


