"""GitHub Actions Remote Execution Backend — free remote runtime for universal mobile project-building.

This module provides the architecture and client for triggering remote work through
GitHub Actions — the cheapest/free practical remote execution path compatible with
the existing Jarvis + GitHub stack.

CLASSIFICATION: REQUIRES_BRYAN_SETUP
COST: FREE (GitHub Actions free tier: 2000 min/month for private repos; unlimited for public)
ACCOUNT REQUIRED: Existing GitHub account (no new account needed)
MACBOOK-OFF CAPABLE: Yes — GitHub runs workflows on GitHub-hosted runners, not on MacBook

WHAT GITHUB ACTIONS CAN DO FOR JARVIS:
  - Run pytest tests on any branch/commit
  - Run build commands (npm build, cargo build, etc.)
  - Run packaging/release checks
  - Generate diffs between branches
  - Store artifacts (test results, build outputs)
  - Post status back via GitHub API (Jarvis polls or receives webhook)
  - Run code review analysis tools
  - Execute any shell command in a sandboxed environment

WHAT IT CANNOT DO (hard limits):
  - Real-time interactive file editing (each workflow is a batch job)
  - Sub-second latency (job queuing adds 10-60s startup time)
  - Access private MacBook-local files (only repo content accessible)
  - Deploy to production without Bryan authorization (hard gate preserved)

SETUP REQUIRED FROM BRYAN:
  1. Replace .env GITHUB_TOKEN with valid Classic PAT (ghp_...) with scopes:
     - gist (for snapshot sync)
     - workflow (for triggering Actions)
     - repo (for repo access, diffs, logs)
  2. Create .github/workflows/jarvis-remote.yml in the repo
     (template generated by this module — does NOT auto-push)
  3. No new accounts needed — uses existing GitHub

WORKFLOW TEMPLATE (safe to generate, not auto-pushed):
  - Trigger: workflow_dispatch with inputs (task_type, task_params)
  - Steps: checkout → install deps → run task → upload artifact → post status
  - All runs are sandboxed on GitHub-hosted runners
  - No secrets exposed in logs (GitHub masks secrets)
  - Bryan authorizes all deploy steps via required_permissions

SECURITY:
  - No secrets in workflow YAML (use GitHub repository secrets)
  - Deploys remain hard-gated (require_approval in workflow)
  - No auto-push, no force-push, no destructive ops without Bryan gate

Sprint: Sprint 3 Consolidated Final Retest
"""

